"""
src/detector/dataset.py
-----------------------
PyTorch detection dataset supporting:
  - CSV annotations (bbox_x1/y1/x2/y2, class_id, image_path)
  - COCO JSON annotations
  - Toy synthetic data (generated by toy_images.py)

Returns torchvision-compatible (image, target) pairs.
"""

import json
from pathlib import Path
from typing import Callable, Dict, List, Optional

import numpy as np
import pandas as pd
import torch
from PIL import Image
from torch.utils.data import Dataset
import torchvision.transforms.functional as TF


# ── Transforms ────────────────────────────────────────────────────────────────

def get_transform(train: bool):
    def _transform(image, target):
        image = TF.to_tensor(image)
        if train:
            # Random horizontal flip
            if torch.rand(1).item() > 0.5:
                image = TF.hflip(image)
                w = image.shape[-1]
                boxes = target["boxes"].clone()
                boxes[:, [0, 2]] = w - boxes[:, [2, 0]]
                target["boxes"] = boxes
        return image, target
    return _transform


# ── CSV detection dataset ─────────────────────────────────────────────────────

class CSVDetectionDataset(Dataset):
    """
    Loads detection data from a CSV.
    Expected columns: image_path, bbox_x1, bbox_y1, bbox_x2, bbox_y2,
                      class_id, image_id
    Optional: patient_id, split
    """

    def __init__(
        self,
        csv_path: str,
        split: Optional[str] = None,
        image_root: Optional[str] = None,
        transforms: Optional[Callable] = None,
    ):
        df = pd.read_csv(csv_path)
        if split and "split" in df.columns:
            df = df[df["split"] == split].reset_index(drop=True)

        self.image_root = Path(image_root) if image_root else None
        self.transforms = transforms

        # Group by image — each __getitem__ is one full image
        self.image_ids = df["image_id"].unique().tolist()
        self.id_to_rows: Dict[str, pd.DataFrame] = {
            iid: df[df["image_id"] == iid] for iid in self.image_ids
        }
        self.df = df

    def __len__(self):
        return len(self.image_ids)

    def __getitem__(self, idx):
        iid  = self.image_ids[idx]
        rows = self.id_to_rows[iid]
        first = rows.iloc[0]

        img_path = first["image_path"]
        if self.image_root:
            img_path = str(self.image_root / img_path)

        image = Image.open(img_path).convert("RGB")

        boxes  = rows[["bbox_x1", "bbox_y1", "bbox_x2", "bbox_y2"]].values
        labels = rows["class_id"].values.astype(np.int64)

        # Clamp boxes to image dimensions
        w, h = image.size
        boxes[:, [0, 2]] = np.clip(boxes[:, [0, 2]], 0, w - 1)
        boxes[:, [1, 3]] = np.clip(boxes[:, [1, 3]], 0, h - 1)

        # Filter degenerate boxes
        valid = (boxes[:, 2] > boxes[:, 0]) & (boxes[:, 3] > boxes[:, 1])
        boxes  = boxes[valid]
        labels = labels[valid]

        target = {
            "boxes":    torch.as_tensor(boxes, dtype=torch.float32),
            "labels":   torch.as_tensor(labels, dtype=torch.int64),
            "image_id": torch.tensor([idx]),
            "image_path": img_path,
            "patient_id": str(first.get("patient_id", "")),
            "split":    str(first.get("split", "")),
        }

        if self.transforms:
            image, target = self.transforms(image, target)

        return image, target


# ── COCO detection dataset ────────────────────────────────────────────────────

class COCODetectionDataset(Dataset):
    """
    Loads detection data from a COCO-format JSON.
    """

    def __init__(
        self,
        json_path: str,
        image_root: str,
        split: Optional[str] = None,
        transforms: Optional[Callable] = None,
    ):
        with open(json_path) as f:
            coco = json.load(f)

        self.image_root = Path(image_root)
        self.transforms = transforms

        id2img = {img["id"]: img for img in coco["images"]}
        id2anns: Dict[int, list] = {}
        for ann in coco.get("annotations", []):
            id2anns.setdefault(ann["image_id"], []).append(ann)

        self.samples = []
        for img_id, img_info in id2img.items():
            anns = id2anns.get(img_id, [])
            self.samples.append({
                "image_id":   img_id,
                "file_name":  img_info["file_name"],
                "annotations": anns,
            })

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        s    = self.samples[idx]
        path = str(self.image_root / s["file_name"])
        image = Image.open(path).convert("RGB")
        w, h  = image.size

        boxes, labels = [], []
        for ann in s["annotations"]:
            x, y, bw, bh = ann["bbox"]   # COCO format: x, y, w, h
            x2, y2 = x + bw, y + bh
            boxes.append([
                np.clip(x, 0, w - 1),
                np.clip(y, 0, h - 1),
                np.clip(x2, 0, w - 1),
                np.clip(y2, 0, h - 1),
            ])
            labels.append(ann.get("category_id", 1))

        if not boxes:
            boxes  = np.zeros((0, 4), dtype=np.float32)
            labels = np.zeros(0, dtype=np.int64)
        else:
            boxes  = np.array(boxes, dtype=np.float32)
            labels = np.array(labels, dtype=np.int64)

        target = {
            "boxes":    torch.as_tensor(boxes, dtype=torch.float32),
            "labels":   torch.as_tensor(labels, dtype=torch.int64),
            "image_id": torch.tensor([s["image_id"]]),
            "image_path": path,
            "patient_id": "",
            "split":    "",
        }

        if self.transforms:
            image, target = self.transforms(image, target)

        return image, target


# ── Collate function ──────────────────────────────────────────────────────────

def collate_fn(batch):
    return tuple(zip(*batch))


# ── CSV → COCO-JSON converter (utility) ──────────────────────────────────────

def csv_to_coco_json(csv_path: str, output_path: str, split: str = "train"):
    """Convert CSV annotations to COCO JSON format."""
    df = pd.read_csv(csv_path)
    if "split" in df.columns:
        df = df[df["split"] == split]

    images, annotations = [], []
    img_id_map = {}
    ann_id = 1

    for _, row in df.iterrows():
        iid_str = str(row["image_id"])
        if iid_str not in img_id_map:
            img_id_map[iid_str] = len(img_id_map) + 1
            images.append({
                "id":        img_id_map[iid_str],
                "file_name": str(row["image_path"]),
            })
        iid = img_id_map[iid_str]
        x1, y1 = float(row["bbox_x1"]), float(row["bbox_y1"])
        x2, y2 = float(row["bbox_x2"]), float(row["bbox_y2"])
        annotations.append({
            "id":          ann_id,
            "image_id":    iid,
            "category_id": int(row.get("class_id", 1)),
            "bbox": [x1, y1, x2 - x1, y2 - y1],
            "area": (x2 - x1) * (y2 - y1),
            "iscrowd": 0,
        })
        ann_id += 1

    coco = {
        "images": images,
        "annotations": annotations,
        "categories": [{"id": 1, "name": "lesion"}],
    }
    Path(output_path).parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w") as f:
        json.dump(coco, f)
    print(f"COCO JSON saved to {output_path} ({len(images)} images, "
          f"{len(annotations)} annotations)")
    return coco
